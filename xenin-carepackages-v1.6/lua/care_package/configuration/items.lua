-- The third argument is a table, and a valid field there is "Chance"
-- By default chance is 100 on all items unless changed.
-- If you set the chance to 10, the item is 10 times less likely to be picked.
-- There's also Color. This will set the color of something. If you have Xenin Inventory the color is based off your inventory config!
